// Java Program to show the use of
// shift operators
import java.io.*;

class Geeks 
{
    public static void main(String[] args)
    {
        int a = 10;
    
        // Using left shift
        System.out.println("a<<1 : " + (a << 1));
      
        // Using right shift
        System.out.println("a>>1 : " + (a >> 1));
    }
}
